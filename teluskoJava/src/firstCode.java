public class firstCode {

	public static void main(String args[]) {
		
		System.out.print("Hello World");
		
		int num = 500_00_0000;    // 4 bytes -> 32 bits -> -2,147,483,648 to 2,147,483,647
		num=8;
		System.out.println(num);
		
		int b=5; 				// 4 bytes -> 32 bits -> -2,147,483,648 to 2,147,483,647	
		System.out.println(b);
		
		float c=5.5f;			// 4 bytes -> 32 bits -> -2,147,483,648 to 2,147,483,647
		System.out.println(c);
		
		double d=5.5;			// 8 bytes -> 64 bits 
		System.out.println(d);		
		}

		
}

